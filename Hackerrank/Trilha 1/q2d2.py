def diagonalDifference(arr):
    # Write your code here
    size = len(arr)
    c = 0
    r = l = 0
    while c < size:
        r = r + arr[c][c]
        l = l + arr[c][size - c - 1]
        c = c + 1
    
    return abs(r - l)
        
